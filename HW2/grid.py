# Flatten each 8-bit byte into individual bits.
flat_bits = [
  int(bit_char)
  for byte in BIT_BYTES.split()
  for bit_char in byte
]

# 67x67 numeric bit array (0/1), filled from top-left row-major.
bit_grid = [[0 for _ in range(GRID_SIZE)] for _ in range(GRID_SIZE)]
for idx, bit in enumerate(flat_bits):
  if idx >= GRID_SIZE * GRID_SIZE:
    break
  row, col = divmod(idx, GRID_SIZE)
  bit_grid[row][col] = bit

# 67x67 color array where 0=white, 1=black.
grid = [
  [WHITE if bit == 0 else BLACK for bit in row_bits]
  for row_bits in bit_grid
]
